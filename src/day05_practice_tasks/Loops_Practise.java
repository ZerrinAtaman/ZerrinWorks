package day05_practice_tasks;

public class Loops_Practise {

    public static void main(String[] args) {


        int i = 1;
        int j;

        while (i <= 6){
            j = 1;
            i++;

            while (j <= 8) {
                System.out.print(" * ");
                j++;
            }
            System.out.println();

        }



    }

        }





